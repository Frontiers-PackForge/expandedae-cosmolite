package lu.kolja.expandedae.enums;

public enum ShowInterfaces {
    /**
     * Show interfaces that are not hidden in the interface configuration terminal.
     */
    VISIBLE,
    /**
     * Show interfaces that are not hidden in interface configuration terminal, and that were not full when the terminal
     * was opened / the setting was set.
     */
    NOT_FULL,
    /**
     * Show all interfaces.
     */
    ALL
}
